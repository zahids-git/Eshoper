package beanpackage;

public class CategoryBean {
		
	private int cid;
	private String cname;
	private int main_catid;
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public int getMain_catid() {
		return main_catid;
	}
	public void setMain_catid(int main_catid) {
		this.main_catid = main_catid;
	}
	
}
